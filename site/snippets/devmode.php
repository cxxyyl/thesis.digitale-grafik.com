While you're in DevMode, you are able to have some fun with commands and custom CSS. 
‾‾‾‾‾‾‾‾     ‾‾‾‾‾‾‾‾‾‾        
Quick Commands – We prepared some quick commands for you. Just type them into the input field
‾‾‾‾‾‾‾‾‾‾‾‾‾‾   and things will happen. Right now, it's mostly color themes, but this can be
expanded in the future by you. The idea is that this version is a framework
which can be expanded/changed by you client-side (on your PC) or also in a more
substantial way by writing a module that would be server-side (for everyone).

Custom CSS – – – Okay, so how does this work? In the DevMode module (JS), there is a function + cookies
‾‾‾‾‾‾‾‾‾‾       that saves everything that you write into a separate <style id="devModeCSS"> 
                           element – except quick commands. The style element is also saved on your PC, 
                           so the changes you make to the CSS are permanent unless you delete the cookies 
                           for this website. Cookies are saved for 365 days. If you visit this website 
                           at least once a year, you're good to go. After that, your customizations will be 
                           deleted, just like deleting your cookies. 
                           
So, customize this site to your heart's content – if you want to. In case you 
made a mistake, you can copy everything inside of <style id="devModeCSS">, 
delete the cookie, and paste it back into the input. This should also work for 
more complex (lots of) CSS written with a code editor. You should be able to
paste a completely new CSS into the input and it should (technically) work. 

I was also thinking about adding extra classes that make styling the basic
elements easier, like "rows", "title", "year", etc. But I'm not sure if it makes
things really easier. There is always the code inspector – so maybeeeeeeeeee?

