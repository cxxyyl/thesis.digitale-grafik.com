    So! Welcome again, dear visitor – to thesis.digitale-grafik.com. I hope you are doing well. Most likely, you're part
    of Klasse Digitale Grafik. As I am not sure when you are reading this, maybe we are studying together right now, 
                    –– or studied ––                (–––– so hi <3 maybe you will see this as soon as it launches?)
    or you will use it as a resource in the future when I have already graduated. So, hi from the past! Hope you're 
    having a good time in class. For everyone

    thesis.digitale-grafik is a resource for KDG. While studying in class during the time from 2022–23, a lot of
    students graduated from the class because it was a bit unclear if it would exist in the future. But with that, 
    a lot of knowledge was lost from the class – which, to me, is a bit sad. In a way, this feels like a very natural 
    process, with new generations of students coming and going, but at the same time, I also want to be able to build 
    on the knowledge of everyone that came before me. If possible, I also want my work to be a resource for 
    others. With the big news of the class being "safe" for the future, I wanted there to be a ?good? way to access 
    everything that was written about in class. So, in a way, this is one big service post for the class before I 
    graduate in 2025. Now the question is how well it will be maintained in the future. But the idea is that it will 
    be mostly student-run, with minimal supervision from Christoph and Konrad. 

        In a way, this whole website is "just" a glorified spreadsheet. There exists a somewhat spotty/lossy version 
    of this site as a Google spreadsheet, but I really hope this website can be a better framework to save, retrieve, 
    and work with all the topics that are and have been relevant in Klasse Digitale Grafik.

    This archive has the following main functions:
        – Make thesis projects more accessible by providing searchable text and keywords 
        – Archive/Download the PDFs of the written thesis part 
        – Archive the thesis website, so it's not gone once alumni decide not to host it forever. 

    The second (personal) reason for this website existing is that it has been my semester project for 2024 and 
    is an exploration of what I think a website can be – or, to be more specific, I wanted to work with ideas I have 
    about websites and coding as a test run for my own thesis project. If you went the extra mile to open up the 
    source code of this website, you would realize that there is a lot more here than just the code for the visual 
    part of the website. For me, that is also the main goal of exploration for this project. With learning how to 
    code (thank you Moritz Ebeling, Christoph Knoth, Jana Reddemann, the Internet, and myself), my view of what a 
    website is has changed a lot over the years. I come from 3D, so in the beginning, I was super interested in 3D 
    on the web. But with coding more, I really started to become invested in the form of "writing" this document or 
    text called a website. In general, I would say this view and project are also related to the poetic web and the 
    "open-sourceness" of code as a practice. Other students from KDG who deal with these topics are, as far as I know, 
    Polina Lobanova and Kim Kleinert. 

        In that regard, things are happening here in the code side of things, rather than only in the graphic part. 
    Design-wise, my idea was to make the website as simple and easy to navigate as possible because it is (at least 
    for me) foremost a tool for accessing information – and I wanted to make this as efficient as possible. This side 
    of the website is SearchMode. In that regard, I think that the website being visually pretty minimal is 
    a good thing. Nevertheless, I think it's good to break with this very focused research tool approach and have 
    some fun –––––––– visually. This is DevMode. So we kind of have this Jekyll and Hyde situation with DevMode 
    making the website as unhinged as you want it to be, and SearchMode being really functional but also 
    influenced a lot by what you do in DevMode.

        My hope for this site is that it will be online for some time and that this first iteration of thesis.digitale 
    will be a framework for other students to build and iterate upon. With this in mind, I set up a few ground rules 
    for myself, but also, in a way, for this website as a whole. I want to keep this website as vanilla as possible – 
    so no libraries, frameworks, and generally as few dependencies as possible – the only one being Kirby. The rest 
    is basic HTML, CSS, and JS (+PHP for Kirby). The main goal is that this website should be able to run with as 
    little maintenance as possible. This also informs the modularity of this website. While developing this, I tried 
    to split up everything into separate modules, so if a feature breaks, it's easier to delete or repair it without 
    other parts of the website breaking as well or everything being tightly integrated. This modularity also opens up 
    the way for new modules contributed by other students – new themes, reskins of the site, and completely new 
    functionalities. 

        All of these are server-side modifications to the website, so they apply to everyone who visits this site. 
    But there is also the possibility for client-side (only for you on your device) modifications – basically, 
    you can customize this website as much as you want locally on your device. This (and a few other things) is the 
    main function of DevMode. You are able to inject your own CSS into the website, which will be saved permanently 
    on your device as a cookie. You can use this to modify this website to make it fit your specific needs, 
    without anybody else needing to intervene. The "only" barrier to entry in this case is understanding how all the 
    classes work and then writing your own CSS for the site – which, in theory, you could then also provide as a 
    module so everyone has access to it. 

        My wish for this website is that it will invite you to spend some time with it here in this written view. 
    Since code was a very frightening thing to me in the past, I hope that this can be a welcoming place to explore. 

    In the other chapters, as well as in all the other CSS and JS files, you will find further documentation for this project.

